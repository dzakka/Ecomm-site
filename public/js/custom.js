function plus(){

    var counter=1;
    var sum=0;
    var a = parseInt(document.getElementById("final").value);
    var sum = a+counter;
    document.getElementById("final").value = sum;

}


function minus(){

    var counter=1;
    var sum=0;
    var a = parseInt(document.getElementById("final").value);
    var sum = a-counter;
    document.getElementById("final").value = sum;

}

